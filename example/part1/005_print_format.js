// #005 포맷팅(Formatting)해서 출력하기

console.log("name:%s", "manju");
console.log("age:%d", 36);
console.log("pi:%d", 3.14);
console.log("math:%d science:%d", 92, 84);
console.log("name:%s age:%d", "manju", 36);
console.log("name:%s math:%d science:%d", "manju", 92, 84);
console.log("name:%s math:%s science:%s", "manju", 92, 84);

